  String call(String key, {Map<String, Object?> params = const {}}) {
    var s = _t[_l]?[key] ?? _t[AppLocale.en]?[key] ?? key;
    params.forEach((k, v) {
      s = s.replaceAll('@$k', '$v');
    });
    return s;
  }

